#include <bits/stdc++.h>

using namespace std;

int algorithm(vector<int>& input, int k)
{
    sort(input.begin(), input.end());
}

int main()
{
    vector<int> input = {1,2,3,4};
    int k = 10;

    cout << algorithm(input, k) << endl;

    return 0;
}